// Helper function to search companies
export function searchCompanies(query: string, limit = 5): KnownCompany[] {
  if (!query || query.length < 2) return [];

  const normalizedQuery = query.toLowerCase().trim();

  return KNOWN_COMPANIES
    .filter(company => {
      const nameMatch = company.name.toLowerCase().includes(normalizedQuery);
      const aliasMatch = company.aliases.some(alias =>
        alias.toLowerCase().includes(normalizedQuery)
      );
      const tickerMatch = company.ticker?.toLowerCase() === normalizedQuery;
      return nameMatch || aliasMatch || tickerMatch;
    })
    .sort((a, b) => {
      // Prioritize exact matches and ticker matches
      const aExact = a.name.toLowerCase().startsWith(normalizedQuery) ||
                     a.ticker?.toLowerCase() === normalizedQuery;
      const bExact = b.name.toLowerCase().startsWith(normalizedQuery) ||
                     b.ticker?.toLowerCase() === normalizedQuery;
      if (aExact && !bExact) return -1;
      if (!aExact && bExact) return 1;
      return 0;
    })
    .slice(0, limit);
}

